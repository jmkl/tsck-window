pub fn parse_style(id: u32) -> String {
    let string_id = format!("UKNOWN {}", id.to_string());
    let result = match id {
        1u32 => "WS_ACTIVECAPTION",
        8388608u32 => "WS_BORDER",
        12582912u32 => "WS_CAPTION",
        1073741824u32 => "WS_CHILDWINDOW",
        33554432u32 => "WS_CLIPCHILDREN",
        67108864u32 => "WS_CLIPSIBLINGS",
        134217728u32 => "WS_DISABLED",
        4194304u32 => "WS_DLGFRAME",
        131072u32 => "WS_GROUP",
        1048576u32 => "WS_HSCROLL",
        536870912u32 => "WS_ICONIC",
        16777216u32 => "WS_MAXIMIZE",
        65536u32 => "WS_MAXIMIZEBOX",
        0u32 => "WS_OVERLAPPED",
        13565952u32 => "WS_OVERLAPPEDWINDOW",
        2147483648u32 => "WS_POPUP",
        2156396544u32 => "WS_POPUPWINDOW",
        262144u32 => "WS_SIZEBOX",
        524288u32 => "WS_SYSMENU",
        268435456u32 => "WS_VISIBLE",
        2097152u32 => "WS_VSCROLL",
        _ => "",
    };
    if result == "" {
        return string_id;
    } else {
        return result.to_string();
    }
}
